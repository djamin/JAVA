package string_code;

public class ToLowercase {

	public static void main(String[] args) {
		
	String name=new String("ruhul amin");
	
	name=name.toLowerCase();//make lower case
	System.out.println(name);
	
	name=name.toUpperCase();//make upper case
	System.out.println(name);
	
	
	
	 StringBuffer A;
	 A=new StringBuffer("ruhul amin");
	A=A.reverse(); // MAKE REVERSE CASE
	System.out.println(A);
	}

}
